# Given variables x = 15 and y = 20, write expressions to check if:
# a. x is equal to y
# b. x is not equal to y
# c. x is greater than y
# d. x is less than or equal to y

x = 15
y = 20

if x == y:
    print(f"{x} is equal to {y}" )
elif x != y:
    print(f"{x} is not equal to {y}" )
elif x > y:
    print(f"{x} is greater than {y}" )
elif x <= y:
    print(f"{x} is less than or equal to {y}" )
else:
    print("error")
    
